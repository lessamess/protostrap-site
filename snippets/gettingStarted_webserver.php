        We recommend to use <a href="https://www.mamp.info">MAMP</a> as a local webserver. MAMP is available for free for macOS and Windows. It is the easiest way to have a running webserver.
        <br><br>
        To get MAMP running:<br>
        <ul class="simpleList">
            <li>Download and install MAMP</li>
            <li>Go to Applications, open the MAMP folder and start MAMP</li>
            <li>Open the MAMP preferences</li>
            <li>Open to the <b>Ports</b> tab</li>
            <li>Click on the <b>Set Web & MySQL port to 80 and 3306</b> Button. <br><div class="micropadding"></div>
                <img class="img-responsive" src="assets/img/mampPrefs.png" alt="Mamp Preferences">
                </li>
            <li>Restart the server. It will not work without restart!</li>
            <li>You now have a local webserver running under <a href="http://localhost">http://localhost</a></li>
        </ul>
